    @FXML
    void Search(MouseEvent event) throws SQLException {
        Statement s = ConnectDB.getConnection();
        ArrayList<Flight> query = new ArrayList<Flight>();
        PreparedStatement p = s.getConnection().prepareStatement("SELECT * " +
                "FROM Flight " +
                "WHERE flight_start_time BETWEEN '"+Depart_Date.getValue().minusDays(5)+ "' " +
                "AND '"+Depart_Date.getValue().plusDays(5)+"' AND flight_from = '"+ Where_From.getText()
                +"' AND flight_to = '"+ Where_To.getText() +"'");
        ResultSet get_p = p.executeQuery();
        while (get_p.next()){
            query.add(new Flight(get_p));
        }
        setFlights(query);
        ArrayList<Node> off= new ArrayList<Node>();
        off.add(Where_From);
        off.add(Where_To);
        off.add(No_of_people);
        off.add(Depart_Date);
        off.add(Economy);
        off.add(Business);
        off.add(Both);
        off.add(Find_Flight);
        off.add(Calendar);
        off.add(passenger);
        off.add(Where);
        off.add(Group);
        off.add(lable);
        for (Node node:off) {
            node.setVisible(false);
            node.setDisable(true);
        }
        book.setVisible(true);
        book.setDisable(false);
        back.setVisible(true);
        back.setDisable(false);
        Results.setVisible(true);
        Results.setDisable(false);
    }

